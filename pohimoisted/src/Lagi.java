public class Lagi {
    private Double korgus;
    private String varv;
    //  konstruktor
    public Lagi(Double korgus, String varv) {
        this.korgus = korgus;
        this.varv = varv;
    }
    //  getters
    public Double getKorgus() {
        return korgus;
    }
    public String getVarv() {
        return varv;
    }
}